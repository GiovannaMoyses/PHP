<?php

//retorna o valor absoluto
$r = abs(-500);
print("A resposta é $r<br>");

//converte números
$b = base_convert(254, 10, 8);
print("A resposta é $b<br>");

//divisão inteira
$d = intdiv(6, 4);
print("A resposta é $d<br>");

//mostra o valor minimo e maximo
$m = min(6, 3);
print("A resposta é $m<br>");
$M = max(20, 12, 23);
print("A resposta é $M<br>");

//mostra o valor de pi
$p = pi();
print("A resposta é $p<br>");

//potencia
$e = pow(6, 2);
print("A resposta é $e<br>");

//raiz quadrada
$q = sqrt(36);
print("A resposta é $q<br>");
